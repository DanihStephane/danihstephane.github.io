// Initialisation
document.addEventListener('DOMContentLoaded', function() {
    const projectLinks = document.querySelectorAll('.project-card');

    projectLinks.forEach((link, index) => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            openModal(projectsData[index]);
        });
    });

    const modalOverlay = document.getElementById('modalOverlay');
    const closeButton = document.querySelector('.modal-close');

    closeButton.addEventListener('click', closeModal);
    modalOverlay.addEventListener('click', (e) => {
        if (e.target === modalOverlay) {
            closeModal();
        }
    });

    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') {
            closeModal();
        }
    });
});

function openModal(project) {
    const modal = document.getElementById('modalOverlay');
    modal.style.display = 'block';

    // Mise à jour du contenu
    document.querySelector('.modal-title').textContent = project.title;
    document.querySelector('.modal-image').src = `${project.image}?v=${Date.now()}`;
    document.querySelector('.modal-image').alt = project.title;
    document.querySelector('.modal-video').src = project.videoUrl;
    document.querySelector('.modal-description').textContent = project.description;
    document.querySelector('.modal-date').textContent = project.date;
    document.querySelector('.github-button').href = project.githubUrl;

    // Gestion de l'affichage des technologies
    const techBadges = {
        html: document.querySelector('.html-badge'),
        css: document.querySelector('.css-badge'),
        javascript: document.querySelector('.js-badge'),
        php: document.querySelector('.php-badge'),
        java: document.querySelector('.java-badge'),
        xml: document.querySelector('.xml-badge'),
        android: document.querySelector('.android-studio-badge'),
        firebase: document.querySelector('.firebase-badge'),
        mongodb: document.querySelector('.mongodb-badge'),
        expressjs: document.querySelector('.expressjs-badge'),
        nodejs: document.querySelector('.nodejs-badge'),
        angular: document.querySelector('.angular-badge'),
        grails: document.querySelector('.grails-badge'),
        groovy: document.querySelector('.groovy-badge'),
        shell: document.querySelector('.shell-badge'),
        jvm: document.querySelector('.jvm-badge'),
        batchfile: document.querySelector('.batchfile-badge'),
        ts: document.querySelector('.ts-badge'),
        scss: document.querySelector('.scss-badge'),
        rn: document.querySelector('.rn-badge'),
        symfony: document.querySelector('.symfony-badge')
    };

    // Afficher/masquer les badges selon les technologies utilisées
    Object.entries(project.technologies).forEach(([tech, used]) => {
        if (techBadges[tech]) {
            techBadges[tech].style.display = used ? 'flex' : 'none';
        }
    });

    document.body.style.overflow = 'hidden';

    // Animation d'entrée
    const modalContent = document.querySelector('.modal');
    modalContent.style.animation = 'slideIn 0.3s cubic-bezier(0.16, 1, 0.3, 1)';
}

function closeModal() {
    const modal = document.getElementById('modalOverlay');
    const modalContent = document.querySelector('.modal');

    modalContent.style.animation = 'slideOut 0.3s ease-in-out';

    setTimeout(() => {
        modal.style.display = 'none';
        modalContent.style.animation = '';
        document.querySelector('.modal-video').src = '';
        document.body.style.overflow = '';
    }, 300);
}